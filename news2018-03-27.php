<h2>
<p><br>	

В период с 20 по 22 марта Госпромнадзором проведена контрольная деятельность с использованием мер профилактического и предупредительного характера. Проведен мониторинг теплоисточников н н.п. Сосновый Бор, Судовица, Страковичи, Корени, Вежны, Прудок, Николаевка, г. Светлогорск котельные по ул. Свердлова, Авиационная, Заводская», а также проведена разъяснительная работа с персоналом котельных о порядке соблюдения требований законодательства в области промышленной безопасности, применения его положений на практике.
<p><br>
Выявленные несоответствия законодательству в области промышленной безопасности руководителю предприятия рекомендовано устранить в установленные сроки. С целью улучшения промышленной безопасности на котельных необходимо усилить контроль за качеством сетевой воды на котельных н.п. Сосновый Бор и котельной №3 н.п. Паричи. С положительной стороны можно отметить подготовку и качество выполняемой работы машинистов-кочегаров Филипчик Н.М., Коваль Н.Н., Акулич П.Н., Кикинёв В.А.
