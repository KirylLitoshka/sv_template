Профсоюзный актив КЖУП «Светочь» г.Светлогорска второй год принимает активное участие в акции за здоровый образ жизни «Меняю сигарету на конфету!». В этом году акция проходит в рамках Всемирного дня некурения. 16 ноября профсоюзные активисты пошли в структурные подразделения предприятия с беседами о вреде курения как для самого человека, так и для окружающих. В ходе подготовки к акции профком подготовил яркие листовки и плакаты, объявившие территорию предприятия «Территорией, свободной от курения». Во время акции все желающие смогли обменять сигареты на конфеты. Сладкий приз получили и члены профсоюза, ведущие здоровый образ жизни.
