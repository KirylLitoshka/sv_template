 	Совет физкультурно-спортивного клуба «Коммунальник» подвел итоги круглогодичной спартакиады и смотра-конкурса на лучшую постановку физкультурно-массовой работы среди структурных подразделений КЖУП «Светочь». В канун дня защитников Отечества в кафе «Маентак» прошло чествование  лучших спортивных коллективов и  спортсменов предприятия.
