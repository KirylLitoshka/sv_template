Генеральный директор Валерий Лупей вручил кубки и грамоты победителям спартакиады и смотра-конкурса – сборным командам цеха водопроводно-канализационного хозяйства, аппарата управления, жилфонда, ремонтно-строительного управления «Светлогорскремстрой». Денежные сертификаты на приобретение спортинвентаря из рук главного бухгалтера Валерия Белегова получили председатели цеховых профсоюзных комитетов. А главный инженер предприятия Елена Селюк вручила благодарственные письма участникам зимней областной спартакиады, в которой наша команда заняла второе место. Завершился праздник веселой дискотечной программой.

