<br><h1><strong>

КЖУП "СВЕТОЧЬ" - ЖКХ, с праздником!

</h1></strong>

<h2>
<p><br>
В минувший выходной свой профессиональный праздник отмечали светлогорские коммунальщики. Программа праздничных мероприятий с учетом мировых тенденций была несколько сокращена, но проходила не менее торжественно. Открывая праздник, генеральный директор КЖУП «Светочь» Валерий Лупей отметил, что в 2019 году коллектив значительно продвинулся в техническом перевооружении, внедрении ряда новейших технологий, выполнил все основные доведенные показатели.
<p><br>
Заслуженные награды в этот день получили более 80 светлогорских коммунальщиков – уборщики территорий и слесари-сантехники, водители и рабочие зеленого строительства, строители и инженерно-технические работники.
<p><br>
Благодарностью Премьер-министра отмечен многолетний труд начальника цеха тепловых сетей В.И. Левковского.
Ряд работников предприятия были отмечены Почетными грамотами Министерства жилищно-коммунального хозяйства, ГО «ЖКХ Гомельской области», Светлогорского райисполкома и районного Совета депутатов.  
Фотографии 11 работников предприятия занесены на Доску Почета.
<p><br>
Но не только производственными показателями  известны светлогорские коммунальщики.   Грамотой республиканского комитета Белорусского профсоюза работников местной промышленности и коммунально-бытовых предприятий награжден инструктор-методист по спорту В.И. Круголь, наградами  областного комитета профсоюза на активную работу по защите трудовых и социально-экономических интересов членов профсоюза  отмечены ведущий специалист отдела кадров О.Астапович, главный бухгалтер профкома С.Абаранок, председатели цеховых профсоюзных организаций  В.Кароткина, В таранчук, С.Стук, Почетными грамотами профсоюзного комитета за активную работу в области охраны труда – С.Белый(САБ) и М.Захаренко (электроцех).
<p><br>
Председатель профкома Г.Маковская




