75 лет назад отгремели бои на территории Светлогорского района.  Но до сих пор измученная земля хранит следы былых сражений. Это не только полузаросшие окопы,  осколки мин и снарядов, но и неизвестные захоронения погибших солдат. В 2018 году отрядом поисковиков на территории Светлогорского района возле деревни Великий Бор было обнаружено неучтенное захоронение солдат и офицеров Красной Армии, умерших после тяжелых ранений. Больше года длилась поисковая операция, завершившаяся траурным митингом у памятника павшим  в деревне В.Бор. Останки 78 бойцов были перезахоронены в братской могиле. Поисковикам удалось установить имена более 60 воинов. Это значит, что еще одна страница тех страшных событий закрыта, а к братской могиле еще много лет будут нести цветы  внуки и правнуки павших героев.
Активное участие в процессе перезахоронения приняли работники КЖУП «Светочь»
