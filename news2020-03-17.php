<h2>
<p><br>
Незаметно изменился наш образ жизни: мы стали покупать, а после выбрасывать все больше вещей, упаковки. За последние 15 лет объем бытовых отходов, вывозимых ежегодно из населенных пунктов Беларуси, вырос в два раза.
<p><br>
Главная причина − короткий срок службы многих изделий: одноразовой посуды, полиэтиленовых пакетов, тары и упаковки потребительских товаров. По оценкам ученых, примерно 40% пластмассовых изделий, упаковки имеют срок службы меньше одного месяца. То есть практически сразу же после использования они попадают в поток отходов. Получается, мы покупаем мусор.
<p><br>
До 4 млрд. фасовочных пакетов в год потребляют в Беларуси из-за бесплатной и неограниченной их выдачи в магазинах.
<p><br>
200 млн. штук одноразовой пластиковой посуды используют белорусы за год.
<p><br>
Проблема излишнего, чрезмерного потребления ресурсов – особенность современного мира. Только на протяжении XX века полезных ископаемых было добыто больше, чем за всю предшествующую историю человечества.
<p><br>
Однако почти все природные ресурсы, которые мы тратим на производство упаковки, одноразовых вещей, не бесконечны. Еще эти ресурсы абсолютно не восстанавливаются или восстанавливаются в миллионы раз медленнее, чем мы их используем.
<p><br>
Природа затратила на образование полезных ископаемых миллионы лет. А человек использует упаковку не больше месяца, а пакет – всего лишь час.
<p><br>
Можем ли мы исправить положение, сохранив современный комфорт жизни?
