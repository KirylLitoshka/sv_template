Охране труда – внимание профсоюза

</strong></h1>

<h2>
<p><br>

Одним из важнейших направлений в деятельности профсоюзного комитета КЖУП «Светочь» г. Светлогорска является осуществление общественного контроля за соблюдением законодательства об охране труда. Помогает в этом более 60 общественных инспекторов по охране труда. Одним из важнейших звеньев в этой работе председатель профкома Галина Маковская считает обучение профсоюзного актива. 
<p><br> В  апреле 2017 г. 28 общественных инспекторов прошли обучение по 8-ми часовой программе в Гомельском областном центре повышения квалификации руководящих работников и специалистов отрасли жилищно-коммунального хозяйства. После обучения был утвержден план работы общественных инспекторов  и приняты критерии оценки их деятельности.
  <p><br>
Согласно плана работы в мае общественным инспекторам поручено осуществить мониторинг пожарной безопасности структурных подразделений. А для того, чтобы они могли грамотно провести его, специалисты службы охраны труда организовали занятия по данной теме и разработали памятки по проверке объектов. До конца мая чек-листы будут сданы в комиссию по охране труда предприятия, которая,  в случае выявления нарушений, выдаст нанимателю рекомендацию.
В профкоме считают, что такая форма работы – научил и дал задание – поможет осуществлять общественный контроль более эффективно.



