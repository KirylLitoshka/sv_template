МЧС информирует - Анонс акции «Не прожигай свою жизнь»

</strong></h1>

<h2>
<p><br>
С целью предупреждения пожаров и гибели людей от них по причине неосторожного обращения с огнем при курении в состоянии алкогольного опьянения с 14 ноября по 01 декабря текущего года будет проходить республиканская акция «Не прожигай свою жизнь. Акция будет проходить в 4 этапа:
<p><br><b>1-й этап – 14 ноября</b> - акция проводится в общественном транспорте, на остановках общественного транспорта.
<p><br><b>2-й этап – 15-22 ноября</b> – акция проводится на промышленных предприятиях и в организациях, подведомственных агрогородках, в пунктах общественного питания.
<p><br><b>3-й этап – 23-28 ноября</b> – акция проводится в ВУЗах, ССУЗах  (лицеи, колледжи).  
<p><br><b>4-й этап – 29 ноября - 1 декабря</b> - акция проводится в местах массового пребывания людей (в крупных торговых центрах, на рынках и т.д.).


