<br><h1><strong> Анонс акции «Безопасный новый год» </strong></h1>
<p><br>
Предновогодний период сопровождается резким увеличением количества пожаров и травм в результате нарушения правил пожарной безопасности при подготовке к праздникам, неосторожного обращения с пиротехническими изделиями. Предупредить чрезвычайные ситуации с подобными последствиями – цель республиканской акции «Безопасный Новый год!», которая пройдет в Светлогорском районе с 4 по 28 декабря.
<p><br>
Акция будет проходить в 4 этапа. 
<p><br>
<b>1-й этап</b> – с 4 по 12 декабря (акция проводится в санаториях, оздоровительных центрах, профилакториях, домах отдыха,  пансионатах, домах ветеранов, центрах социального обслуживания населения). 
<p><br>
<b>2-й этап</b> – с 13 по 20 декабря (акция проводится в общеобразовательных учреждениях, интернатах, учреждениях внешкольной работы (центры творчества детей и молодежи,  школы развития, дворцы детей и молодежи, и т.п.),  домах семейного типа, семьях, где дети находятся в социально опасном положении).
<p><br>
<b>3-й этап</b> -  с 21 по 26 декабря (акция проводится в местах продажи пиротехнических изделий, на закрытых и открытых ледовых катках, в детских учреждених здравоохранения).
<p><br>
<b>4-й этап</b> – с 27 по 28 декабря (акция проводится в местах массового пребывания людей – открытые площадки, крупные торговые центры, рынки, детские развлекательные центры и т.д.).
 

