Коммунальные отходы – новые подходы!!


</strong></h1>

<h2>
<p><br>

В конце прошлого года жители сектора индивидуальной застройки в г. Светлогорске  получили такие же квитанции за коммунальные услуги, как и в многоквартирных  жилых домах, что связано с переходом на единую общереспубликанскую информационную систему расчетов за потребленные жилищно-коммунальные и другие услуги. Перемены сразу же привели к росту недовольных. Если раньше в частном секторе за каждую услугу платили отдельно и народ «экономил» на вывозе мусора, аргументировав тем, что нет договора, то теперь эту услугу стало сложно игнорировать. Она вписана в общую жировку вместе со всеми остальными. 
<p><br>
У многих стал возникать вопрос: А почему я должна (или должен) платить за то, чем не пользуюсь? - Бумагу, пластик и металл мы сдаем в заготовительные пункты, а растительность идет в компост. Ничего лишнего не остается.
Но для коммунальников такая позиция не повод для освобождения от начислений за вывоз и утилизацию отходов. Это обязательная услуга, которая регламентирована жилищно-коммунальным законодательством. Но у некоторых жителей желание найти лазейку уж очень велико.
<p><br>
Если потребитель хочет отказаться от услуги на основании того, что кто-то, кто зарегистрирован на его жилой площади, там не живет, работники жилищно-коммунального хозяйства готовы пойти навстречу, но должны знать, куда тот человек переехал, чтобы начислить ему сумму по другому адресу. Если он вообще уехал из города на длительный срок, должны быть подтверждающие этот факт документы. Так требует закон.
В соответствии с решением Светлогорского районного исполнительного комитета от 27.07.2009 № 1185 норма образования коммунальных отходов неблагоустроенного жилого здания, находящегося в собственности граждан 2 кубических метра в год на 1 человека. 
<p><br>
<!-- <img src="img/news04.04.17_02.jpg" width="700">  -->
<p><br>
На сегодняшний день решением Гомельского областного исполнительного комитета от 23.03.2016 № 255 «О некоторых вопросах установления фиксированных тарифов на услуги по обращению с твердыми коммунальными отходами, оказываемые населению» установлены предельно допустимые тарифы (цены) на жилищно-коммунальные услуги и размеры возмещения расходов организаций, осуществляющих эксплуатацию жилищного фонда и (или) предоставляющих жилищно-коммунальные услуги: субсидируемый государством тариф (цена) – 5,6655 руб. за 1 м3.
<p><br>
Следовательно, граждане, проживающие или имеющие в собственности индивидуальные жилые дома, обязаны заключить с организацией жилищно-коммунального хозяйства договоры на оказание услуги по вывозу и обезвреживанию твердых бытовых отходов и производить регулярную оплату за вывоз (п. 52, гл. 12 Положения о порядке расчетов и внесения платы за жилищно-коммунальные услуги и платы за пользование жилыми помещениями государственного жилищного фонда, утвержденного постановлением Совета Министров Республики Беларусь 12.06.2014 № 571) по следующим основаниям: 
<p><br>
<p>- свои отходы потребления граждане обязаны размещать в санкционированных местах временного хранения (свалка ТКО по г. Светлогорску);
<p><br>
<p>- услуга по вывозу и обезвреживанию твердых бытовых отходов является основной жилищно-коммунальной услугой, у КЖУП «Светочь» отсутствует техническая возможность, на практике, не оказывать названную услугу конкретному потребителю и при этом лишить его фактической возможности пользоваться этой услугой;
<p><br>
В связи с тем, что граждане, проживающие в частном секторе, зачастую используют контейнерные площадки не по назначению, складируя вокруг площадки отходы, не подлежащие централизованному сбору, что влечет за собой штрафные санкции со стороны контролирующих органов, КЖУП «Светочь» приняло решение о приобретении новой специализированной техники для сбора ТКО в частном секторе и демонтаже контейнерных площадок. 
В настоящее время разрабатывается график вывоза отходов, с указанием конкретного дня и времени, который будет опубликован на сайте нашего предприятия позднее.   
