<p><br>2.3. отходы, содержащие токсичные вещества и вещества с высокой реакционной способностью: тяжелые металлы, галогенные соединения, кислоты, щелочи, соли, ароматические углеводороды (аккумуляторы, элементы электропитания, ртутные, люминесцентные, энергоэкономные лампы, термометры, фотохимикаты, кинескопы и другие элементы радиоэлектронной аппаратуры, аэрозольные баллончики и другое), средства для борьбы с сорняками, вредителями растений, грызунами, паразитами и тара из-под них
