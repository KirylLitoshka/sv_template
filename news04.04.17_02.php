<p><br>1. Отходы, которые по своему виду, количеству и параметрам не могут собираться в контейнеры, предусмотренные для сбора:
<p><br>1.1. отходы от обслуживания земельных участков, предоставленных или переданных для строительства и (или) обслуживания жилого дома, ведения личного подсобного хозяйства и (или) огородничества, в том числе отходы от обрезки (рубки) зеленых насаждений, содержания и убоя скота, сезонной санитарной уборки
<p><br>1.2. строительные отходы
<p><br>1.3. крупногабаритные отходы, лом черных и цветных металлов
<p><br>1.4. автомобильные шины
