<br><h1><strong>Профсоюз - Благотворительная акция «ДОРОГАМИ ДОБРА» </strong></h1>
<h2>	
<p><br>
Ежегодно в канун Новогодних и Рождественских праздников помочь детям, оставшимся без родительского тепла и мечтающих о чуде, вызываются сотни людей - сотрудники крупных организаций, общественных объединений, местных исполнительных органов.
<p><br>
Профсоюзный комитет  КЖУП "Светочь" не остался в стороне и в очередной раз принял  участие в новогодней благотворительной акции «ДОРОГАМИ ДОБРА».  
<p><br>
22 декабря работники предприятия стали участниками благотворительного рождественского концерта, все средства от которого пошли на укрепление материально-технической базы  социально-педагогического центра г. Светлогорска.
<p><br>
29 декабря воспитанники социально-педагогического центра пригласили нас на свой праздник. Дети показали и свои таланты, устроив замечательное новогоднее костюмированное представление, и красочный танцевальный «флешмоб».
<p><br>
Председатель профкома Маковская Г.В. и секретарь первичной организации "БРСМ" Плашкова И.И. подготовили и вручили подарки ребятам, создав  атмосферу настоящего новогоднего праздника с хорошим настроением и позитивными эмоциями.
<p><br>
Завершились новогодние и рождественские праздники  большим новогодним утренником, в котором приняли участие более 150 детей работников предприятия.
Акция «Дорогами добра» принесла положительные  эмоции не только детям, но и взрослым участникам, ведь видеть радость и улыбки на детских лицам – это так приятно!
<p><br>
Автор: Ирина Плашкова, член профкома КЖУП «Светочь», председатель Совета молодежи.
<p><br>
</h2>
