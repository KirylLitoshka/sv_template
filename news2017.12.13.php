<br><h1><strong>Новости профкома - «Миссис Светлогорск-2017» работает в коммунхозе</strong></h1>
<p><br>

<h2>	
Конкурс красоты и материнства прошел 8 декабря в Светлогорском центре культуры. Впервые да сцену вышло 11  замужних женщин, чтобы продемонстрировать красоту, грацию, изящество.
Первый выход участниц стал своеобразной визитной карточкой: после великолепного дефиле в шикарных вечерних нарядах каждая из конкурсанток вкратце рассказала о себе, своей семье, увлечениях и мечтах.
<p><br>
Затем участницы шоу демонстрировали наряды из льняной  коллекции белорусских дизайнеров, участвовали в зажигательном флэшмобе, на несколько минут превратились в истинных парижанок в маленьких черных платьях от Шанель.
<p><br>
Кульминацией вечера стал выход участниц вместе со своими детьми Зрители конкурса буквально искупали в аплодисментах маленькие сокровища мамочек.
<p><br>
Каждая из участниц по итогам конкурса получила признание жюри в одной из номинаций: «Миссис-грация», «Миссис очарование», «Миссис-кокетка» и др.
<p><br>
Корону победительницы примерила на семя специалист по коммунальным расчетам расчетно-справочного центра КЖУП «Светочь» Анна Храпатая.  К слову, мнение жюри совпало с мнением более, чем двух тысяч светлогорцев, которые отдали Анне победу в номинации «Приз зрительских симпатий».
<p><br>
Победа Анюты далеко не случайна. Работники предприятия знают ее, как активную участницу наших концертных программ, ведь она много лет занимается хореографией. А в 2015 году  в конкурсе «Мисс-ЖКХ» она получила титул «Мисс-грация».
<p><br>
Мы поздравляем Анну с заслуженной победой и уверены в дальнейшем сотрудничества с красавицей при проведении наших мероприятий.
<p><br>
