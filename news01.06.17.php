Пропаганда здорового образа жизни всегда являлась одним из направлений деятельности профсоюзов. Для членов профсоюза КЖУП «Светочь» это не только систематические занятия физкультурой и спортом в спортивном комплексе и бассейне, оздоровительные поездки во время отпуска, но и участие в различных профилактических акциях, как, например, «Скажи сигарете нет!» с успехом прошедшей в ноябре 2016 года, скринингу на наличие ВИЧ-инфекции в декабре и мн.других.
 <p><br> 
25 мая на предприятии прошла информационно-образовательная акция по профилактике  болезней системы кровообращения «Цифры здоровья: артериальное давление». В ходе акции всем желающим была предоставлена возможность  измерить артериальное давление, получить консультацию врача-специалиста. Работниками зонального центра гигиены и эпидемиологии был проведен «круглый стол» по профилактике болезней кровообращения.
Подобные акции, по мнению профсоюзного комитета, способствуют повышению информационной грамотности по вопросам формирования здорового образа жизни.


