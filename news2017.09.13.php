<h2>
<p><br>

В Беларуси проиндексируют субсидируемые государством цены на природный и сжиженный газ, тарифы на электрическую энергию и тепловую энергию для нужд отопления и горячего водоснабжения для населения. Это предусмотрено постановлением Совета Министров от 24 августа 2017 года №641. Документ вступает в силу с 1 сентября. Таким образом, с начала месяца цены для населения на газ природный и сжиженный, тарифы на электрическую и тепловую энергию увеличиваются. Тарифы вырастут на 8%.



