<h1><strong>

Первый в Беларуси экстремальный бег с препятствиями

</strong></h1>

<h2>

<p><br>
В России, Беларуси, Европе, Канаде и других странах все большую популярность набирают гонки с препятствиями. Такие как BisonRace (Беларусь), ZonaRace(Беларусь), Гонка Героев (Россия), The OCR European Championships (Европа) и др.
<p><br>
Bison Race – первая в Беларуси гонка с препятствиями, которая проводится с 2015 года. Соревнования представляют собой смесь трейлового бега и кроссфита. Участник должен за минимальное время преодолеть трассу, на которой размещаются искусственные и природные препятствия.
<p><br>
Важное преимущество соревнований по экстрим бегу перед обычными соревнованиями по кроссу в том, что это довольно-таки веселое занятие, и основная масса участников приходит сюда развлечься.

<p><br>
Трасса была проложена по горной пересеченной местности. Расстояние трассы 12 километров и 45 препятствий, такие как: рукоходы, высокие барьеры, колючая проволока, тяжелые мешки, грязь, ныряние в ледяную воду, огонь и т.д.
<p><br>
В данных соревнованиях, при поддержке профкома, проверял себя на прочность, на длинной дистанции в «Элитном забеге» работник КЖУП «Светочь» Постол Александр.

<p><br>
