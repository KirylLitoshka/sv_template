<p><br>	

Проехав в начале мая по всем городам и поселкам Гомельщины, велосипедное ралли докатилось и до Светлогорска. 12 мая 2018 года работники КЖУП «СВЕТОЧЬ» приняли участие в велопробеге, посвященном Дню Государственного герба и Государственного флага Республики Беларусь. Велопробег стартовал в 11.00 с Центральной площади. Преодолев маршрут в 16 км,  участники финишировали плотной группой на площадке у кафе «Маёнтак», где спортсмены могли отдохнуть и перекусить. Здесь же всех гостей праздника ждали концертная программа и награждение.  Памятный приз получил старейший участник велогонки, один из ведущих спортсменов предприятия Анатолий Шатов.
