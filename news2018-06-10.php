<p><br>	

Коммунальники идут навстречу как забывчивым гражданам, так и тем, кто не смог заплатить за квартиру по уважительной причине. Так при оплате основного долга в полном объеме существует возможность списания пени, если на это будут объективные обстоятельства. Законодательством Республики Беларусь оговорена возможность списания пени гражданам, имеющим просроченную задолженность, которая производится по решению Светлогорского районного исполнительного комитета, согласованного с Гомельским облисполкомом.
Уважаемые жители нашего города и района! Чтобы избежать вышеуказанных мер и не попасть в трудно исправимую ситуацию, необходимо оплачивать коммунальные услуги вовремя и в полном объеме. Не забывайте об этом. 








