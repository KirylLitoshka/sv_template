<p>Премьер-министр поручил межведомственной рабочей группе 25 августа проработать и внести в Совет Министров предложения:

<p><br>- о порядке, размерах и сроках изменения тарифов на жилищно-коммунальные услуги для населения;
<p><br>- о дополнительных системных мерах государственной адресной социальной поддержки различных категорий населения при оплате жилищно-коммунальных услуг с использованием безналичных жилищных субсидий, об объемах и возможных источниках финансирования такой поддержки;
<p><br>- о компенсационных механизмах оказания помощи гражданам, которые не в состоянии оплатить жилищно-коммунальные услуги по устанавливаемым для населения тарифам с учетом начисленных безналичных жилищных субсидий и других мер государственной адресной социальной поддержки.
<p><br>
<p>При выработке предложений рабочая группа должна учитывать, достаточно ли средств на жизнь остается у граждан после оплаты услуг ЖКХ и других обязательных платежей, включая платежи по погашению кредитов на строительство жилья.
