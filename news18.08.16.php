<p><br>
<p>Решение о создании межведомственной рабочей группы для выработки дополнительных мер государственной адресной социальной поддержки граждан при оплате ими жилищно-коммунальных услуг принял Премьер-министр Андрей Кобяков распоряжением от 9 августа 2016 г. № 327р., сообщает Национальный центр правовой информации.
<p><br>
