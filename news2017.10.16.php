<p><br>

Раздел жилой площади осуществляется собственником (собственниками — если жилье находится в долевой собственности, например, раздел совместно нажитого имущества супругов) жилого помещения.
<p><br>

При наличии технической возможности на перепланировку жилого помещения возможен раздел с образованием двух и более отдельных жилых помещений (например, каждому из супругов достается доля в квартире для перепланировки и создания отдельно жилого помещения).
<p><br>

При отсутствии согласия со стороны других собственников (если такое жилье находится у них в долях) раздел имущества (жилой площади) осуществляется в судебном порядке.
<p><br>

В случаях отсутствия технической возможности на перепланировку жилья с образованием нескольких жилых помещений собственники (если жилье находится в долевой собственности) могут произвести раздел дома или квартиры помещения с выделением им отдельных комнат. В данном случае все подсобные помещения (коридоры, санузел, кладовая, прихожая и т.п.) будут находиться в общем пользовании у всех собственников жилья.
<p><br>

Выделение комнаты (комнат) при разделе жилого помещения возможно в случае, если они изолированы (не являются смежными).

