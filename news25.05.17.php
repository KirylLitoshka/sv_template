Оплата за жку. Разъяснения

</strong></h1>

<h2>
<p><br>


 <p><br> 
<b>ВОПРОС:</b>
 <p><br> 
Добрый день! Поясните еще такую ситуацию. У тети квартира общей площадью 67.8 м2. Договор найма мы заключили на одну комнату жилой площадью 17 м2. Я должна зарегистрироваться на площадь 17 м2? Как в этом случае будет начисляться плата за ЖКУ. Должны ли у нас быть разделенные лицевые счета? И как будет рассчитывается плата за ЖКУ за места общего пользования (кухня, ванная, туалет, коридор и т.д.), с учетом удельного веса жилой площади комнаты в общей жилой площади? Должны ли эти условия отражаться в договоре найма? 
 <p><br> 
<b>ОТВЕТ:</b>
 <p><br> 
Здравствуйте. 
 <p><br> 
Для оплаты жилищно-коммунальных услуг по субсидируемым тарифам вы должны зарегистрироваться в жилом помещении по месту жительства.
Местом жительства и регистрации может выступать только жилое помещение с определенным адресом, в вашем случае – квартира (п. 4.5  Положения о регистрации граждан по месту жительства и месту пребывания, утвержденного Указом Президента Республики Беларусь от 07.09.2007 № 413).
Регистрация на «площадь 17 м. кв.» невозможна.
 <p><br> 
Оплата за отдельные ЖКУ зависит от количества граждан, зарегистрированных в жилом помещении по месту жительства.
Поскольку в данном случае вы будете зарегистрированы в жилом помещении по месту жительства, то плата за ЖКУ будет рассчитываться в общем порядке, исходя из объемов потребления и регистрации 1 человека.
Законодательство не предусматривает отдельную оплату ЖКУ за места общего пользования (кухня, ванная, туалет, коридор и т.д.), поскольку ЖКУ предоставляются в отношении жилого помещения (квартиры), а не отдельных комнат или мест общего пользования.
 <p><br> 
Отметим, что порядок и условия оплаты ЖКУ должен быть предусмотрен в договоре найма (ст. 50 Жилищного кодекса Республики Беларусь).
Например, в договоре между вами и тетей вы можете указать, что определенные ЖКУ (например, отопление) вы и тетя будете оплачивать пропорционально: вы – 25 %, а тетя – 75 %.
Вы можете указать в договоре особенности оплаты всех ЖКУ.
Но это урегулирование только ваших с тетей отношений.
 <p><br> 
Извещение о размере платы за ЖКУ все равно будет одно, исходя из платы за ЖКУ в отношении всей квартиры.
Разделение лицевых счетов возможно только в квартире, которая находится в долевой собственности нескольких лиц. В данном случае Вы не собственник, поэтому лицевой счет может быть только один.

