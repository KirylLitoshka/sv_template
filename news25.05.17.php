 <p><br> 
<b>ВОПРОС:</b>
 <p><br> 
Добрый день! Прошу пояснить порядок оплаты за ЖКУ если собственником жилого помещения является моя тетя, которая там не зарегистрирована, а зарегистрирована я (ее племянница). И я плачу по тарифам, обеспечивающих ЭОЗ. Если мы оформим договор найма частного жилищного фонда и зарегистрируем его в исполкоме, буду ли я оплачивать ЖКУ по субсидированным тарифам. Спасибо! 

 <p><br> 
<b>ОТВЕТ:</b>
 <p><br> 
Здравствуйте.
 <p><br> 
Согласно п. 12 Положения о порядке расчетов и внесения платы за жилищно-коммунальные услуги и платы за пользование жилыми помещениями государственного жилищного фонда, утвержденного Постановлением Совета Министров Республики Беларусь от 12.06.2014 № 571, плата за техническое обслуживание, газо-, электро- и теплоснабжение, снабжение сжиженным углеводородным газом от индивидуальных баллонных или резервуарных установок, горячее водоснабжение вносится плательщиками жилищно-коммунальных услуг по установленным законодательством тарифам (ценам), обеспечивающим полное возмещение экономически обоснованных затрат на оказание таких услуг, в случае:
наличия у плательщика жилищно-коммунальных услуг - гражданина в собственности и (или) во владении и пользовании одного или нескольких жилых помещений (долей в праве общей собственности на жилые помещения) и отсутствия в них (в одном из них) зарегистрированных по месту жительства плательщика жилищно-коммунальных услуг и (или) членов его семьи, за исключением случаев, указанных в пункте 12-2, пункте 20 (в отношении технического обслуживания), абзацах третьем - девятом пункта 20 (в отношении теплоснабжения), пункте 36 (в отношении горячего водоснабжения) и частях первой - третьей пункта 44 (в отношении электроснабжения) настоящего Положения.
 <p><br> 
Плательщик жилищно-коммунальных услуг - собственник жилого и (или) нежилого помещения, наниматель, поднаниматель жилого помещения, арендатор жилого помещения, член организации застройщиков, дольщик, заключивший договор, предусматривающий передачу ему во владение и пользование объекта долевого строительства (при оказании услуг электроснабжения - абонент, услуг газоснабжения - потребитель газа) (п. 2 Положения № 571).
Таким образом, если Вы заключите договор найма с тетей, зарегистрируете его, и сами на основании него зарегистрируетесь в данном жилом помещении по месту жительства, то Вы будете оплачивать ЖКУ по субсидируемым тарифам.

