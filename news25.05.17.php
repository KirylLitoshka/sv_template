 <p><br> 
Поскольку Вы не наниматель жилого помещения, в жилом помещении не зарегистрирован собственник и члены его семьи, то Вы действительно должны оплачивать ЖКУ по установленным законодательством тарифам (ценам), обеспечивающим полное возмещение экономически обоснованных затрат на оказание таких услуг.
 <p><br> 
