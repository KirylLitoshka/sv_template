<p>В целях реализации утвержденной программы, а также во исполнение п. 21 Плана действий Гомельского областного исполнительного комитета и Министерства жилищно-коммунального хозяйства Республики Беларусь по выполнению в 2015 году заданий Программы социально-экономического развития, других государственных программ и поручений Правительства Республики Беларусь в сфере жилищно-коммунального хозяйства, а также предложений рабочей группы по проблемным вопросам в жилищно-коммунальном хозяйстве, утвержденном Министром жилищно-коммунального хозяйства Республики Беларусь А.А. Тереховым и Председателем Гомельского областного исполнительного комитета В.А. Дворником в марте т.г., КДЖЭУП «Жилфонд» производятся работы по завариванию мусоропроводов. 
