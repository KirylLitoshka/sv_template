<div><a href="#open23" onclick="show('hidden_23',600,5)">23. Имеет ли право предприятие жилищно-коммунального хозяйство отключить мне электроэнергию на квартиру за наличие задолженности за другие жилищно-коммунальные услуги? </div> 
<div id=hidden_23 style="display:none;width:720px;background-color:#fdfcee">
<p><br>


</span></a>

Согласно пункту 27 статьи 1 Жилищного кодекса Республики Беларусь к коммунальным услугам относятся:  горячее и холодное водоснабжение, водоотведение (канализация), газо-, электро-, теплоснабжение, пользование лифтом, вывоз, обезвреживание и переработка твердых коммунальных отходов.
<p><br>
В соответствии с частью первой статьи 16 Закона Республики Беларусь «О защите прав потребителей жилищно-коммунальных услуг» коммунальные услуги оказываются исполнителем потребителю постоянно, за исключением перерывов, предусмотренных частью второй настоящей статьи, а в отношении электроснабжения - за исключением перерывов, предусмотренных законодательными актами.
Вместе с тем, организации системы жилищно-коммунального хозяйства не являются организациями, осуществляющими предоставление услуги электроснабжения, но являются исполнителями основной жилищно-коммунальной услуги – техническое обслуживание жилого дома. 
<p><br>
Согласно типовому договору на оказание услуг по техническому обслуживанию жилого дома, вывозу, обезвреживанию и переработке твердых коммунальных отходов и пользованию лифтом, утвержденному постановлением Совета Министров Республики Беларусь от 27 января 2009 г. № 99 "О мерах по реализации Закона Республики Беларусь "О защите прав потребителей жилищно-коммунальных услуг" (далее – типовой договор), исполнитель обязан производить в соответствии с требованиями технических нормативных правовых актов техническое обслуживание общего имущества и обеспечивать его содержание в исправном состоянии, в том числе – инженерных систем электроснабжения (сетей электроснабжения от вводного распределительного устройства до этажного (квартирного)  электрощитка, отключающей и защитной аппаратуры подачи электроэнергии в квартиру).
Таким образом, содержание систем электроснабжения входит в объем услуг, предоставляемых организациями системы жилищно-коммунального хозяйства.
<p><br>
При этом согласно подпункту 4.2 указанного типового договора исполнитель вправе в случае наличия задолженности Потребителя по плате за услуги по техническому обслуживанию жилого дома, вывозу, обезвреживанию и переработке твердых  коммунальных  отходов, пользованию лифтом в порядке, установленном законодательством,  принимать  меры к ее взысканию, а также уменьшать объем предоставляемых услуг до полного погашения задолженности Потребителем.
<p><br>
Указанный типовой договор является публичным. Согласно пункту 4 статьи 396 Гражданского кодекса Республики Беларусь в случаях, предусмотренных законодательными актами, Правительство Республики Беларусь может издавать правила, обязательные для сторон при заключении и исполнении публичных договоров (типовые договоры, положения и т.п.), если иное не установлено Президентом Республики Беларусь.
Учитывая вышеизложенное, организации системы жилищно-коммунального хозяйства в соответствии с положениями типового договора вправе уменьшать объем предоставляемых услуг по техническому обслуживанию жилого дома посредством не осуществления отдельных видов работ, в частности – обслуживания инженерных систем электроснабжения (сетей электроснабжения от вводного распределительного устройства до этажного (квартирного)  электрощитка, отключающей и защитной аппаратуры подачи электроэнергии в квартиру).
