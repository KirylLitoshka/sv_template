<div><a href="#open19" onclick="show('hidden_19',670,5)">19. По вопросу оплаты за водоснабжение и водоотведение в ЕРИП разъясняем следующее.</div> 
<div id=hidden_19 style="display:none;width:720px;background-color:#fdfcee">
<p><br>


</span></a>

В соответствии с п.28 Положения о порядке расчетов и внесения платы за жилищно-коммунальные услуги и платы за пользование жилыми помещениями государственного жилищного фонда, утвержденного постановление Совета Министров Республики Беларусь от 12.06.2014 №571 (далее – Положение), плата за коммунальные услуги исчисляется исходя из фактического потребления этих услуг в натуральном выражении на основании данных приборов индивидуального учета, а также тарифов на коммунальные услуги, установленных в соответствии с законодательными актами.
<p><br>
Снятие и регистрация показаний приборов индивидуального учета расхода холодной, горячей воды производится потребителем самостоятельно.
<p><br>
В соответствии с п. 36 Положения плата за услуги водоснабжения, водоотведения (канализации) в жилых домах (жилых и нежилых помещениях), оснащенных приборами индивидуального учета расхода воды, взимается с плательщиков жилищно-коммунальных услуг по субсидируемым тарифам для населения - до 140 литров в сутки на одного зарегистрированного по месту жительства или месту пребывания (по выбору плательщика жилищно-коммунальных услуг и (или) иного гражданина на основании письменного заявления, представляемого исполнителю по форме, утвержденной Министерством жилищно-коммунального хозяйства, и документов, подтверждающих регистрацию по месту пребывания). Плата за услуги водоотведения (канализации) вносится плательщиком жилищно-коммунальных услуг исходя из суммарного объема потребленной холодной и горячей воды и тарифа за 1 куб. метр отводимых стоков в канализацию.
<p><br>
При оплате за услуги водоснабжения в КЖУП «Светочь» потребитель может представить показания прибора учета с дробными долями, например за июнь месяц текущего года показания прибора учета по горячей воде - 233,34, по холодной воде – 247,25, за июль текущего года по горячей воде - 234,52, по холодной воде - 248,90. То к оплате будут приняты и, соответственно, будет начислена плата за горячую воду за 1,18 м3, за холодную воду – 1,65 м3, водоотведение – 2,83 м3.
<p><br>
При этом плата, за фактически потребленные услуги, на данном примере, будет взиматься по субсидируемым тарифам для населения.
<p><br>
Вносить плату наличными возможно, на основании квитанции абонентного отдела, в кассу КЖУП «Светочь» (ул. Социалистическая, д. 54) в рабочие дни с 8.30 до 13.00 и с 14.00 до 17.30, за исключением последнего рабочего дня месяца.
<p><br>
