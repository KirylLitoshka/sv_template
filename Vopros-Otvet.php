<div><a href="#open24" onclick="show('hidden_24',400,5)">24. Через полгода вступаю в наследство (а именно - в права на приватизированную квартиру) и хотелось бы узнать, кто должен вносить плату за жилищно-коммунальные услуги и по каким тарифам будет осуществляться начисление платы за это время? </div> 
<div id=hidden_24 style="display:none;width:720px;background-color:#fdfcee">
<p><br>


</span></a>


Согласно пункту 13 Положения о порядке расчета и внесения платы за жилищно-коммунальные услуги и платы за пользование жилыми помещениями государственного жилищного фонда, утвержденного постановлением Совета Министров Республики Беларусь от 12 июня 2014г. № 571 (далее – Положение), в случае принятия наследства задолженность по плате за жилищно-коммунальные услуги и (или) плате за пользование жилым помещением погашают наследники в порядке, установленном в статье 1086 Гражданского кодекса Республики Беларусь. 

<p><br>
При этом плата за жилищно-коммунальные услуги, подлежащая внесению наследниками, принявшими жилое помещение в составе наследства, рассчитывается по субсидируемым тарифам для населения без начисления пеней до получения свидетельства о праве на наследство, но не более шести месяцев со дня открытия наследства.

<p><br>
Согласно статье 1086 Гражданского кодекса Республики Беларусь каждый из наследников, принявших наследство, отвечает по долгам наследодателя в пределах стоимости перешедшего к нему имущества.

<p><br>
Обязанностью собственника жилого помещения является внесение платы за жилищно-коммунальные услуги, в том числе за капитальный ремонт (статьи 29, 31 Жилищного кодекса Республики Беларусь).

<p><br>
Учитывая вышеизложенное, обязанностью (долгом) собственника жилого помещения является внесение платы за жилищно-коммунальные услуги, в том числе и за капитальный ремонт. После смерти собственника жилого помещения данная обязанность (долг) переходит в соответствии с нормами действующего законодательства к его наследникам.

<p><br>
Кроме того, услуги по техническому обслуживанию жилого дома, отоплению жилого помещения оказываются организациями независимо от количества человек, проживающих в жилом помещении.

<p><br>
Таким образом, Вы, как наследник жилого помещения, должны внести плату за жилищно-коммунальные услуги.



