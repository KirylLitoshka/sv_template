Указом Президента Республики Беларусь от 23.03.2016 №107 «Об оплате жилищно-коммунальных услуг, оказываемых населению» (с дополнениями и изменениями) установлены предельно допустимые тарифы (цены) на жилищно-коммунальные услуги и размеры возмещения расходов организаций, осуществляющих эксплуатацию жилищного фонда и (или) предоставляющих жилищно-коммунальные услуги, на электроэнергию, потребляемую на освещение вспомогательных помещений и работу оборудования в многоквартирных жилых домах, в размере 0,39 рубля на одного проживающего в месяц. Как правило, данная сумма не обеспечивает полное возмещение затрат на освещение вспомогательных помещений многоквартирных жилых домов.
<p><br>
Указами Президента Республики Беларусь от 31.12.2015 №535 «О предоставлении жилищно-коммунальных услуг» и от 23 марта 2016 года №107 «Об оплате жилищно-коммунальных услуг, оказываемых населению» предусмотрено возмещение расходов на электроэнергию потребляемую на освещение вспомогательных помещений и работу оборудования. 	Согласно Жилищному кодексу Республики Беларусь от 28 августа 2012 года №428-3, вспомогательными считаются помещения, расположенные внутри блокированного либо многоквартирного жилого дома вне квартир, предназначенные для обеспечения эксплуатации жилого дома (вестибюль, коридор, галерея, лестничные марши и площадки, лифтовые холлы и другие помещения). Индивидуальные подвальные ячейки не являются вспомогательными помещениями. 
<p><br>
На основании вышеизложенного, руководствуясь письмом Государственного объединения «Жилищно-коммунальное хозяйство Гомельской области» от 30.09.2016 №02-33/144 об отключении выявленного осветительного оборудования, подключенного в подвальных помещениях от приборов учета электроэнергии, потребляемой на освещение вспомогательных помещений, ведется плановое отключение освещение индивидуальных подвальных ячеек в многоквартирных жилых домах, в которых не обеспечивается полное возмещение затрат на освещение вспомогательных помещений жилого дома.
<p><br>
Собственники индивидуальных подвальных ячеек могут обратиться в КЖУП «Светочь» (ул. Социалистическая, д. 54, каб. 204) с заявлением на выдачу технических условий на электроснабжение данных ячеек, с установкой электросчетчика.

