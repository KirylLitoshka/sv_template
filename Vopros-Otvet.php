<p><br>
Формулировка предельно ясна. Поквартирная разводка трубопроводов горячей и холодной воды вместе с приборами учета, кранами, вентилями и всем иным сантехническим оборудованием принадлежит владельцу жилья. И устанавливается ИПУ исключительно в собственных интересах, чтобы не платить за воду по экономически обоснованным тарифам, а получить право на субсидирование в определенных размерах. Почему же о сохранении этого права должен заботиться кто-то другой? Если в личном автомобиле неисправен спидометр, никому ведь не придет в голову предъявлять претензии госавтоинспекции: отремонтируйте, тогда и буду соблюдать скоростной режим. В случае с водой примерно такая же ситуация: хочешь жить по общепринятым правилам, позаботься сам об их сохранении.
<p><br>
<b> Водосчетчики приобретаются и устанавливаются за счет средств абонента и являются его собственностью. Профилактическое обслуживание, ремонт, замену и поверку водомеров проводят службы жилищно-коммунального хозяйства по заявке абонента и за счет его средств.
</b><p><br>
Алгоритм заботы прост: потребитель покупает индивидуальный прибор учета, обращается в эксплуатаирующую организацию или к любому индивидуальному предпринимателю, имеющему соответствующую лицензию. После установки ИПУ заключается договор с водоканалом на оказание услуг по водоснабжению и водоотведению. И лишь после взятия на коммерческий учет оборудование считается официально узаконенным.
<p><br>
К слову, в отличие от автомобильного спидометра квартирный водомер действительно может обслуживаться бесплатно. На безвозмездной основе раз в 6 лет проводится плановая поверка ИПУ, во время которой производится замена прибора из обменного фонда предприятия (для города Минска). Возможно, этот нюанс и служит для некоторых потребителей поводом к сомнениям: раз устанавливают свои приборы учета, значит, и отвечают за их исправность.
