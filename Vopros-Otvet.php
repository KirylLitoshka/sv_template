Искать виновных пост-фактум действительно нелегко. Ведь трещины и разрывы могут произойти из-за чего угодно. Трубопроводы имеют свойство создавать напряжение (сжиматься, растягиваться), вода в них из-за износа может быть не лучшего качества, при отключении воды не исключена возможность так называемого гидравлического удара и т. д. Как правило, в «разборе полетов» участвуют три стороны: жилищно-эксплутационная служба, водоканал и потребитель. Чтобы в этом споре рассчитывать на положительный для себя результат, потребитель, прежде всего, должен соблюдать правила эксплуатации.
