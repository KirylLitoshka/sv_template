<h2>

<p><br>
25 октября закончилось первенство г. Светлогорска среди коллективов физической культуры предприятий и организаций города.  В финальном матче встретились команды КЖУП «Светочь» и районного отдела МЧС. С первых минут коммунальники повели в счете и не дали соперникам ни единого шанса на победу. Открыл счет Андрей Купчик, водитель спецавтобазы, а во втором тайме отличился  Владислав Згурский, работник ремонтно-строительного управления «Светлогорскремстрой». Со счетом 2:0 команда КЖУП «Светочь» стала чемпионом города по мини-футболу. К слову, это не первая серьезная победа светлогорских коммунальщиков – они неоднократные  победители областной отраслевой спартакиады среди работников ЖКХ.
<p><br>
