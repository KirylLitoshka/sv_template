    <br>
    <p><em><strong>— Кто в Беларуси может осуществлять управление общим имуществом многоквартирного жилого дома? </strong></em></p>
    <br>
    <p><strong>Е.К.:</strong> Это могут быть участники домовладения, товарищество собственников или организация застройщиков, а в случае, когда способ управления не был выбран,&nbsp;— государственная служба заказчика (ред.: в Минске&nbsp;— КУП&nbsp;«ЖКХ»). Или, к примеру, если в товариществе не обеспечивается надлежащее управление, то районная администрация вправе назначить уполномоченное лицо по управлению общим имуществом. В&nbsp;Минске эту роль выполняют государственные службы заказчика. И,&nbsp;как правило, в&nbsp;основном они управляют общим имуществом старого жилфонда, в&nbsp;новостройках же все чаще люди решают создавать товарищества.</p>
    <br>
    <p><strong>В.У.</strong>: В моей практике был случай, когда руководитель кооператива, созданного еще в советские времена, уже не мог в силу возраста исполнять свои обязанности. Собрать общее собрание, чтобы решить вопрос об&nbsp;управлении общим имуществом, ему также не удавалось ввиду незаинтересованности собственников, поэтому он попросту передал всю документацию в районную администрацию.</p>
