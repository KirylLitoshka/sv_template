    <p><strong>Е.К.:</strong> Устав товарищества собственников не может противоречить действующему законодательству, а в Беларуси действуют Правила пользования жилыми помещениями многоквартирного жилого дома, в&nbsp;которых все эти нюансы оговорены. Нужно просто уметь идти навстречу друг другу. Ведь можно понять, что у соседа юбилей. Но&nbsp;тем не менее встречаются случаи, когда люди ходят и прислушиваются, чуть ли шумы не записывают, потому что испытывают личную неприязнь к соседу.</p>
