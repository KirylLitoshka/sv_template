    <p><em><strong>— Может ли одно товарищество собственников объединять несколько многоквартирных жилых домов?</strong></em></p>
    <br>
    <p><strong>Е.К.:</strong> Сегодня это законодательством не предусмотрено, но в практике встречаются случаи, когда несколько домов, находящихся на одной территории, объединены в одно товарищество. Мы планируем развить это направление в будущем, однако у подобных объединений тоже есть свои минусы. К&nbsp;тому же сегодня наше население не особо проявляет инициативу даже в рамках своего дома, и им по большому счету все равно, что происходит в соседнем.</p>
    <br>
    <p><strong>В.У.:</strong> Представьте, что рядом стоят два дома, один из которых был построен в 2011&nbsp;году, а другой&nbsp;— в 76-м или 2000-м. Это ведь совершенно разные дома, у них абсолютно разные конструкции и степень износа. Именно по&nbsp;этим причинам объединение собственников этих домов будет достаточно проблематичным. А&nbsp;вот покупать изначально несколько домов, имеющих что-то общее, исходя из экономической целесообразности, намного выгоднее.</p>
    <br>
    <p><strong>Г.К.:</strong> Объединения собственников нескольких жилых домов&nbsp;— это по сути китайская практика, там на общем участке земли строятся целые жилые кварталы. В&nbsp;качестве положительного примера в Беларуси можно привести квартал «Зеленый бор», где проживает около 12&nbsp;000&nbsp;граждан, где есть собственная диспетчерская служба, техника, видеонаблюдение, и у людей нет причин говорить, что это плохо. Такие объединения могут оправдать себя лишь в том случае, если на общем участке земли примерно в одно время будут строиться однотипные дома, но создавать подобное из разных домов совершенно неприемлемо. Причем стоит отметить, что в Китае такую практику стали применять именно из-за экономической выгоды: чем больше квартир, тем меньше затрат на юридическое лицо.</p>
