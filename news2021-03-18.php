    <p><em><strong>— Может быть, стоит создать какое-то единое уполномоченное лицо по&nbsp;управлению общим имуществом?</strong></em></p>
    <br>
    <p><strong>Г.К.: </strong>Между собой часто сравнивают товарищества и ЖЭУ, и многие считают, что было бы неплохо сделать все либо государственным, либо отдать в руки частникам. Это неправильно: в каждом случае собственники сами должны определиться&nbsp;— многоэтажки принадлежат им. Кроме того, люди везде работают по одним и тем же правилам. Просто стоит всегда помнить о том, что специалисты&nbsp;— главное в этой сфере, и постоянно уменьшать им зарплату, тем самым снижая затраты, нельзя. Потому что потом, даже вливая в эту отрасль огромные деньги, понадобится много лет, чтобы восстановить высокий уровень профессионализма работников ЖКХ.</p>
