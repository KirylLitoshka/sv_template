    <p><em><strong>— Если все имущество является собственностью людей, то кто тогда зарабатывает на рекламе в лифтах?</strong></em></p>
    <br>
    <p><strong>Г.К.:</strong> Если собственники проявили активность и создали, к примеру, товарищество, то эти деньги будут идти им, на счет товарищества. А&nbsp;если они пассивны, то все решения принимает государственная служба заказчика, но у собственников есть возможность в любой день собраться и принять решение о направлении этих средств. А&nbsp;то по логике многих получается, что уполномоченное лицо не только должно стараться заработать лишние деньги на содержание общего имущества, но еще и раздавать их собственникам? Это был бы слишком большой коммунизм.</p>
