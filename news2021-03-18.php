    <br>
    <p><strong>В.У.:</strong> Мне жильцы часто жалуются, что детям днем не дают поспать. И&nbsp;я сам иду в квартиру, где делается ремонт, и, как правило, рабочие соглашаются сделать перерыв на определенное время. Нужно уметь договариваться, своих собственников я прошу не конфликтовать между собой, а приходить ко мне с&nbsp;подобными вопросами.</p>
