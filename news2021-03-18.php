    <p>
        <em><strong>— Есть ли разница между товариществом собственников и организацией застройщиков?</strong></em>
    </p>
    <br>
    <p><strong>Г.К.:</strong> Чтобы никто не путался, стоит начать с того, что фактически все многоэтажные дома в Беларуси, за исключением арендного жилья,&nbsp;— это совместные домовладения. И&nbsp;в Жилищном кодексе прописано, что формой управления совместным домовладением может быть любая структура: и&nbsp;товарищество собственников, и организация застройщиков, несмотря на&nbsp;некоторые нюансы в их деятельности.</p>
    <br>
    <p><strong>В.У.:</strong> Во многом эти формы перекликаются, но главное отличие состоит в&nbsp;том, что организация застройщиков создается еще на стадии строительства жилого дома, а товарищество уже после ввода дома в эксплуатацию. Суть не&nbsp;меняется, наша организация как была ЖСПК-820, так ею и осталась, выполняя те же функции, что и товарищество собственников. Конечно, когда человек приобретает квартиру в ТС, то он автоматически становится членом товарищества, а чтобы стать членом ЖСПК, необходимо заплатить вступительный взнос. То&nbsp;есть, пока человек его не оплатит, у него не будет права голоса на общих собраниях. И&nbsp;главное, что хотелось бы отметить, выбирать форму управления стоит на стадии строительства дома, когда люди еще очень активны и практически 100% посещают общие собрания. После сдачи дома сделать это будет гораздо сложнее.</p>
    <br>
    <p><strong>Е.К.:</strong> Организация застройщиков создается для строительства многоквартирного жилого дома, но с возможностью последующего управления общим имуществом. Товарищество же изначально создается в&nbsp;целях сохранения и содержания общего имущества. Что касается приобретения квартиры в ЖСПК и автоматического вступления в члены кооператива, то этот вопрос в скором времени будет урегулирован законодательно.</p>
