    <br>
    <p><strong>Г.К.:</strong> При покупке квартиры человек отдает 90% стоимости именно за общее имущество. Если нет общего имущества, то нет и жилого помещения. Только представьте, если в доме убрать все стены и перекрытия, что останется от&nbsp;квартиры? Каждый собственник является совладельцем имущества, и,&nbsp;когда люди говорят, что это не мое, это абсурд, так быть не может даже по&nbsp;бухгалтерским документам, не касаясь законодательства. В&nbsp;свою очередь и многие белорусские чиновники часто используют такую фразу, как «дом находится на балансе ЖКХ», что вводит людей в заблуждение. Они начинают думать, что этот дом коммунальный и у них в собственности только квартира. Нужно называть вещи своими именами: дом находится в&nbsp;оперативном управлении у такой-то организации, а не на балансе. Ведь будь эти дома на балансе, то организации ЖКХ могли бы напрямую обращаться в международные кредитные организации, потому что это миллиарды долларов недвижимости.
