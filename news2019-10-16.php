</h1></strong>

<h2>
<p><br>
Водогрейные приборы отопления (котлы), на каком бы виде топлива они ни работали, при неправильном обращении с ними могут нести потенциальную опасность. Связано это со многими факторами и ситуациями, которыми может сопровождаться эксплуатация котлов. Производители современного котельного оборудования стараются оснастить изделия все более совершенной автоматикой, которая, без сомнения, намного повышает безопасность подобных изделий во время их работы. Однако никакая автоматика не способна гарантировать абсолютную безопасность, если не будут соблюдены установленные правила при монтаже водогрейного оборудования и, особенно, при его эксплуатации.
<p><br>
Представители соответствующих организаций контролируют правила установки только газовых водогрейных приборов. Котлы же, работающие на других видах топлива, устанавливаются владельцами самостоятельно. Часто единственным источником информации (инструкцией) является только руководство по эксплуатации, сопровождающее изделие при его покупке. По идее, в каждой инструкции должны быть описаны и правила обращения с прибором, соблюдение которых может гарантировать владельцу определенную степень безопасности. Там же указывается и срок, в течение которого это изделие можно безопасно эксплуатировать.
<p><br>
В начале этого отопительного сезона чрезвычайных ситуаций со взрывами бытовых котлов зафиксировано не было. 
<p><br>
Несмотря на положительные температуры наружного воздуха в дневные часы, в ночное время столбик термометра опускается ниже нулевой отметки. Данный факт ослабляет бдительность субъектов хозяйствования и граждан, эксплуатирующих бытовые котлы, в том числе и в частных домовладениях.
<p><br>
При таких колебаниях температур наружного воздуха, нередки случаи замерзаний систем отопления (расширительных баков) с прекращением циркуляции воды в отопительной системе и, как следствие, взрывы котлов.
<p><br>
Замерзание систем отопления, как правило, происходит в чердачных помещениях при не утепленных или недостаточно утепленных расширительных баках, в тех случаях, когда котлы эксплуатируются на твердых видах топлива не постоянно, или при наличии сквозняков, воздействующих на систему отопления.
<p><br>
Напомним, что в 2018 году, произошел ряд чрезвычайных ситуаций (взрывы отопительных котлов) в помещениях жилых домов частных домовладений. В результате взрывов котлов разрушены стены топочных помещений, повреждены кровли. Пострадали жители домов. 
<p><br>
