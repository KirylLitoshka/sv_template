<p><br>
Причиной взрывов котлов явилось отсутствие циркуляции воды в системе.
<p><br>
С целью недопущения и профилактики подобных несчастных случаев, Гомельским областным управлением Госпромнадзора  постоянно проводится работа с субъектами хозяйствования по доведению информации о безопасной эксплуатации бытовых котлов, работающих на твердом и газообразном топливе, в том числе вручаются памятки населению.
<p><br>
В очередной раз хочется напомнить простые требования, понимание и выполнение которых поможет избежать чрезвычайных ситуаций.
<p><br>
По уровню потенциальной опасности на первое место можно поставить водогрейные котлы, использующие в качестве теплоносителя природный газ.
<p><br>
Здесь основную опасность представляет как сам газ, утечка которого может привести к взрыву, так и продукты горения, способные вызвать отравление. Поэтому правила, описанные в соответствующих инструкциях, направлены на предупреждение именно таких ситуаций.
<p><br>
Более безопасными в плане эксплуатации являются газовые устройства, оборудованные закрытой топкой, полость которой не сообщается с воздушной средой помещения, где расположен котел. Однако такие генераторы тепловой энергии ограничены в максимальной мощности. Поэтому часто приходится в частных домовладениях устанавливать котлы с открытой камерой горения. Для установки и последующей эксплуатации такого типа водогрейного оборудования существуют определенные правила. Благодаря автоматике происходит перекрывание подачи газа при спонтанном затухании горелки или, например, поддержание теплоносителя в определенных температурных пределах, что предупреждает перегрев жидкости. Повышает степень безопасности при эксплуатации газовых приборов установка газоанализатора - прибора, который не только сигнализирует о загазованности помещения, но и способен прекратить подачу газа.
<p><br>
Вторыми после газовых (по уровню потенциальной опасности) являются агрегаты, работающие на твердом топливе. Установка такого оборудования в частных домах не регламентирована. То есть здесь безопасность использования котла напрямую зависит исключительно от правильных действий владельца.
<p><br>
В этом случае опасность может представлять отсутствие необходимой тяги, когда может произойти выброс угарного газа внутрь помещения. Чтобы этого не произошло, следует правильно обустраивать дымоход и вовремя очищать его от накопившейся сажи.
<p><br>
Помимо этого, должна быть обеспечена циркуляция воды в системе и непосредственно через сам котел.
<p><br>
Также определенную опасность могут таить выпавшие из топки раскаленные угли. Поэтому стоит позаботиться о том, чтобы площадка перед котлом была выполнена из огнеупорного материала.
Кроме того, желательно поместить котельное оборудование в обособленном помещении, которое не сообщается с жилыми комнатами.
