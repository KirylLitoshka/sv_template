<p><br>

19 мая 2020 года в 10.00 состоится совместное заседание президиума Светлогорского районного Совета депутатов и Светлогорского районного исполнительного комитета по вопросу «Об образовании Светлогорской районной комиссии по выборам Президента Республики Беларусь» в режиме прямой видеотрансляции.
<p><br>

В соответствии с постановлением Центральной комиссии Республики Беларусь по выборам и проведению республиканских референдумов от 8 мая 2020 г. № 13 заседание проводится в отсутствие представителей политических партий, других общественных объединений, трудовых коллективов, граждан, средств массовой информации и других приглашенных лиц.
<p><br>

Заседание состоится по адресу: г. Светлогорск, пл. Центральная, 1, (Светлогорский райисполком), зал заседаний (второй этаж).
