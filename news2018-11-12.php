<h2>
<p><br>	
Тарифы на ЖКУ в 2019 году вырастут не более чем на $5 в эквиваленте, сообщил журналистам заместитель премьер-министра Владимир Кухарев, передает корреспондент БЕЛТА.
<p><br>	

