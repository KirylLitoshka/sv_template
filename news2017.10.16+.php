<h1><strong>

МЧС информирует - Акция «За безопасность вместе»

</strong></h1>

<h2>
<p><br>

С 16 октября по 14 ноября в Светлогорском районе пройдет акция «За безопасность вместе». Работники районного отдела по чрезвычайным ситуациям путем профилактических бесед будут информировать население о причинах возникновения пожаров, об опасности оставления детей без присмотра, об опасности выжигание сухой растительности. Дадут разъяснения и рекомендации гражданам по повышению уровня пожарной безопасности. Посетят по месту жительства одиноких и одиноко проживающих престарелых граждан и инвалидов, семьи, в которых дети находятся в социально-опасном положении.
<p><br>
В профилактической работе задействованы все субъекты профилактики, поэтому будут проведены совместные рейды по проверке противопожарного состояния мест общего пользования в многоквартирных домах. 
<p><br>
В период проведения акции каждый гражданин может обратиться в территориальный орган по чрезвычайным ситуациям для проведения обследования противопожарного состояния жилища.


