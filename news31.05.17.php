<h2>
<p><br>


 <p><br> 
В мае 2017г на конференции трудового коллектива КЖУП «Светочь» принят новый коллективный договор.  Выступая на конференции, председатель профкома Галина Маковская отметила огромное значение коллективного договора в жизни предприятия. По сути- это главный нормативный акт предприятия, своеобразная малая Конституция. В действовавшем коллективном договоре достаточно полно  были представлены все направления деятельности администрации и профсоюзного комитета по обеспечению социальных гарантий всех слоев трудящихся: молодежи, ветеранов, многодетных и малообеспеченных семей, вопросы быта и организации отдыха. 
  <p><br>
При разработке коллективного договора комиссия постаралась не только максимально сохранить прежние социальные гарантии, но и с учетом отраслевых Соглашений, увеличить их. Так, например, при заключении контракта вновь принятый работник получит 3 дополнительных дня к отпуску, а имеющий стаж работы в отрасли более 5 лет – 5 дней. Увеличен размер материальной помощи к отпуску, предусмотрена оплата учебных отпусков и многое другое. Текст нового коллективного договора доступен для всех работников на сайте  предприятия.

