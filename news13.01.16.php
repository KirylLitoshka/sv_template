<p>10 января 2016г. в Светлогорске проходил 1 этап 17-й зимней областной отраслевой спартакиады работников жилищно-коммунального хозяйства Гомельской области.
<p><br>
<p>Сборная команда предприятия на соревнованиях по 8 видам спорта показала следующие результаты:
<p><br>

