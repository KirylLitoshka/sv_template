<p>1 место – плаванье (С.Стук, А.Берлина, Ю.Киселев, В.Гончаров)
<p>1 место – дартс (С.Шафоренко, С.Полетаев)
<p>2 место – бильярд (М.Шукан, Д.Лопатин)
<p>2 место – стрельба (Т.Полуян, И.Бутько
<p>2 место – плавательная эстафета(С.Стук, А.Берлина, Ю.Киселев, В.Гончаров)
<p>2 место – шашки (Т.Надточаева,Н.Шурпо)
<p>2 место – шахматы (Я.Евзрезова, А.Вежновец)
<p><br>
<p>По итогам 1 этапа наша сборная уверенно лидирует с отрывом в 14 очков. Впереди – соревнования по лыжам и боулингу.
<p><br>
<p>ЖЕЛАЕМ НАШИМ РЕБЯТАМ УСПЕХОВ!
<p><br>
<p>Соревнования первого этапа Спартакиады работников ЖКХ области прошли в минувшие выходные в Светлогорске. 
С 8 по 10 января представители 31 команды предприятий коммунхоза Гомельщины приняли участие в состязаниях 
по дартсу, настольному теннису, стрельбе, шашкам и шахматам, плаванию и бильярду.
<p><br>
<p>«В соревнованиях в Светлогорске приняло участие свыше 600 человек, – сказала председатель профсоюзного 
комитета КЖУП «Светочь» Галина Маковская. – Отрадно, что наше предприятие лидирует в командном зачёте: 
у нас в восьми видах спорта первые места по дартсу, теннису и плаванию, третье по бильярду, остальные –
 вторые. К слову, команда КЖУП «Светочь» три года подряд – в 2013, 2014 и 2015 годах – была победителем
  зимней областной Спартакиады, а в прошлом году выиграла ещё и летнюю».
<p><br>
<p>Местом проведения соревнований по дартсу, настольному теннису и стрельбе стал бывший стадион «Труд»
 ОАО «Светлогорский завод ЖБИиК» за автостанцией. Переданный в сентябре прошлого года на баланс 
 районного коммунхоза, спортивный объект стал физкультурно-оздоровительным комплексом КЖУП «Светочь».
<p><br>
 <p>В настольные игры коммунальщики играли в помещении ДЮСШ №5 (ул. Школьная, 3), плавали – в бассейне 
 СДЮШОР №1, а соревнования по бильярду проходили в специализированном клубе бара «Континент».
По информации председателя профкома КЖУП «Светочь», существенный вклад в спортивный успех команды 
внесли пловцы Светлана Стук, Юрий Киселёв, Василий Гончаров и Анна Берлина, мастера дартса Сергей 
олетаев и Светлана Шафоренко, Николай Шурпо (шашки) и Марина Шукан (бильярд). Ядвига Евзрезова стала
 чемпионом области по шахматам, а Иван Бутько – по стрельбе.
<p><br>
<p>«Все наши спортсмены – просто молодцы, – сказала Маковская. – Окончательные итоги зимней Спартакиады
работников ЖКХ области будут подведены позже, после проведения соревнований по лыжным гонкам и 
