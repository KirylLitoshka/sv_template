<h1><strong>



Да здравствует турслет!



</strong></h1>

<h2>
<p><br>

В минувшие выходные на озере Добром в Рогачевском районе прошел первый областной туристический слет 1 группы работников жилищно-коммунального хозяйства.  Турслеты – давняя традиция областного комитета профсоюза. Но до нынешнего  года в них участвовали работники малочисленных ЖКХ области. Эти летом на туристическую тропу вышли представители Рогачева, Гомеля, Жлобина, Мозыря, Калинковичей, Речицы и Светлогорска – всего  10 команд.
 <p><br> 
Рогачевская земля встретила туристов, как и полагается, проливным дождем, но даже и он не смог остудить накал спортивных страстей. В течение трех дней команды соревновались в легкоатлетическом кроссе, спортивном ориентировании, преодолении полосы препятствий, рыбной ловле, пляжном волейболе, велосипедном кроссе и фигурной езде на велосипеде.  Традиционно были проведены соревнования среди руководителей и председателей профсоюзных комитетов, которые на этот раз соревновались в игре в петанк. Каждое утро начиналось с конкурсной зарядки «фитнесс-драйв», а заканчивался день музыкальными представлениями команд и конкурсом бардовской песни.  К слову сказать, это единственный конкурс, в подведении итогов которого  принимали участие сами туристы. Мнение было единодушным - лучший певец турслета – Николай Шурпо из светлогорской команды.
 <p><br> 
Именно светлогорцы и стали первыми победителями туристического слета. И по суммарному зачету  зимних и летних соревнований команда КЖУП «Светочь» стала лучшей в областном комитете профсоюза. Второе места заняла команда Рогачевского ЖКХ, на третьем – КЖУП «Уником» г.Жлобин.
 <p><br> 
От имени   светлогорской команды мы поздравляем всех участников туристического слета и его организаторов – обком профсоюза местной промышленности и коммунально-бытовых предприятий с праздником здоровья, хорошего настроения, бодрости. Благодарим хозяев турслета – команду Рогачевского ЖКХ за отличную подготовку территории.
 <p><br> 
Первый блин не стал комом, все команды при подведении итогов  решили: турслету быть!

