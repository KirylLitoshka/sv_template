    </p>
    <br>
    <p>
        Право на перерасчет платы за жилищно-коммунальные услуги имеют граждане, выехавшие из места постоянной регистрации на срок более 10 суток подряд. При этом факт отсутствия должен быть подтвержден документально. И документы должны быть предоставлены не позднее 7 календарных дней с момента возвращения к месту постоянной регистрации.
    </p>
    <br>
    <p>
        Но надо помнить что, если вы съездили, скажем, на дачу на 5 дней в начале месяца, а потом еще на 5 - в конце, плату за жилищно-коммунальные услуги Вам не пересчитают.
    </p>
    <br>
    <p>
        В Положении 1466 сказано, что об отсутствии надо предварительно поставить в известность организацию, которая начисляет плату за коммунальные услуги. Приостановка оказания услуг ЖКХ — это административная процедура, осуществление которой проводится в соответствии с прописанными в законе требованиями. 
    </p>
    <br>
    <p>
