    <p>
        <b>Какие документы могут служить основанием для перерасчета?</b>
    </p>
    <br>
    <p>
        Полный перечень оснований и необходимых документов для проведения перерасчета платы за коммунальные услуги определен Приложением к Положению 1466. С документом можно ознакомиться на Национальном правовом Интернет-портале <a href="https://pravo.by/ ">pravo.by</a>
    </p>
    <br>
    <p>
        Приведем несколько примеров.
    </p>
    <br>
    <p>
        Выезд за границу – представляется документ, <b>подтверждающий нахождение за границей</b> (именные проездные билеты и (или) документ, удостоверяющий личность, или документ для выезда из Республики Беларусь и (или) въезда в Республику Беларусь с отметками о пересечении границы или регистрации по месту жительства за границей, или другие документы, подтверждающие нахождение за границей, <b>на государственных языках Республики Беларусь и заверенные надлежащим образом)</b>
    </p>
    <br>
    <p>
