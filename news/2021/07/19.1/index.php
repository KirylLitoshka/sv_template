    </p>
    <br>
    <p>
        Пересчитают плату за жилищно-коммунальные услуги и тогда, когда, например, семья имеет квартиру (зарегистрирована) в одном городе, а временно живет и работает в другом. Основанием будет проживание по договорам найма жилого помещения частного жилищного фонда, поднайма жилого помещения государственного жилищного фонда. Подтверждающим документом в данной ситуации будет справка, выдаваемой организацией, насчитывающей плату за коммунальные услуги по месту фактического проживания.
    </p>
    <br>
    <p>
        Иногда в качестве доказательства того, что в квартире определенное время никто не проживал, предоставляется справка Департамента охраны МВД Республики Беларусь. Аргумент вроде бы логичный: если помещение находится на сигнализации, то проникнуть в него незаметно не получится. Однако в Положении 1466 нет ни слова о таком документе, а значит, он не может являться основанием для перерасчета.
    </p>
    <br>
    <br>
    <p>
        <b>А что делать, если гражданин не можешь принести заявление лично?</b>
    </p>
    <br>
    <p>
        Перерасчет за коммунальные услуги производится по заявительному принципу, поэтому работник РСЦ при осуществлении данной процедуры должен идентифицировать личность человека, подающего заявление, по паспорту. По закону представлять интересы другого гражданина можно только на основании нотариально удостоверенной доверенности. 
    </p>
    <br>
    <br>
    <p>
        <b>
            Как определяется время отсутствия по основному месту жительства (регистрации)?
        </b>
    </p>
    <br>
    <p>
        Все очень просто. По датам, указанным в справках, билетах и регистрационных штампах. Например, в справке из больницы будут указаны две даты: когда пациент поступил на лечение и когда его выписали. В проездных билетах указан день отлета и прилета, в договоре найма — срок действия договора. Эти даты включительно и будут взяты за основу для расчетов.
    </p>
    <br>
    <br>
    <p>
        <b>На какой срок можно приостановить оказание коммунальных услуг, в случае если заранее поставить в известность организацию, которая начисляет плату за коммунальные услуги? </b>
    </p>
    <br>
    <p>
        В профильном законодательстве не прописаны эти сроки. Но если гражданин уехал в другую страну работать по контракту на длительное время, то специалисты расчетного центра просят представлять документы, которые это подтверждают, как минимум раз в три года (но можно и чаще). Дело в том, что срок исковой давности, за время которого возможен перерасчет, составляет именно три года (ст. 197 Гражданского кодекса Республики Беларусь). 
Если работа жильца квартиры связана с постоянными разъездами, то документы нужно подавать за каждый период отсутствия по месту жительства.
    </p>
    <br>
    <br>
    <p>
        <b>Давайте подведем итог вышесказанного.</b>
    </p>
    <br>
    <p>
        Из всего вышесказанного следует, что документы, подтверждающие отсутствие по основному месту жительства, вкупе с заявлением являются краеугольным камнем процедуры перерасчета. Но для сохранения их действенности необходимо еще одно важное условие: согласно Положению 1466, «данные документы предоставляются не позднее 7 календарных дней со дня возвращения гражданина к месту жительства».
    </p>
    <br>
    <p>
        Эта норма иногда игнорируется заявителями, и, даже немного опоздав с официальным обращением, непунктуальные граждане ставят коммунальных работников в щекотливое положение. Когда все документы в порядке, то отказывать в перерасчете, по сути, нет оснований, и ссылки на несоблюдение временных рамок выглядят как излишний бюрократизм. К слову, в таких ситуациях суды, как правило, становятся на сторону граждан, и подобные решения еще больше вселяют сомнения в специалистов.
    </p>
    <br>
    <p>
        Но работники расчетно-справочных центров должны неукоснительно соблюдать букву закона и руководствоваться нормативными документами. Понятно, что ситуации бывают разные, и без индивидуального подхода к каждому человеку не обойтись. Но любая импровизация, пусть даже и с самыми лучшими намерениями, может быть истолкована контролирующими органами как нецелевое использование средств. Поэтому в спорных случаях лучше оставить решение на усмотрение суда, вердикт которого узаконит дальнейшие действия.
    </p>
    <br>
    <br>
    <p>
        <b>
            Есть ли экономия? Сколько же вычтут из «жировки», если человек  уехал на месяц (при условии, что установлены все ИПУ)?
        </b>
    </p>
    <br>
    <p>
        Оплата электроэнергии на работу лифта привязана к количеству зарегистрированных, а также проживающих по договору найма жильцов. Но она не может быть выше 0,79 рубля с одного проживающего в месяц.
    </p>
    <br>
    <p>
        Плата за вывоз и обезвреживание мусора также зависит от числа проживающих в квартире человек. Сумма начислений по этой услуге в Светлогорске - 1,04 рубля с человека.  Считаем: 0,79 + 1,04 = 1,83 рубля. Вот эту сумму вычтут из «жировки» на одного человека за две эти услуги.
    </p>
    <br>
    <p>
        Если же у вас нет счетчика на газ, вам удастся сэкономить больше. Такая же ситуация и с водой.
    </p>
    <p>
        Приведем расчет. 
    </p>
    <p>
        1,1623 рубля (тариф услуг водоснабжения, обеспечивающий полное возмещение экономически обоснованных затрат) + 0,972 рубля (тариф услуг канализации, обеспечивающий полное возмещение экономически обоснованных затрат) * 9 м3 (норма на человека) = 19,21 рубля в месяц с одного проживающего.
    </p>
    <br>
    <br>
    <p>
        <b>
            Так кому же все-таки выгодно делать перерасчет?
        </b>
    </p>
    <br>
    <p>
        Как видно из примеров, такой перерасчет выгоден тем, кто длительно не проживает в своей квартире. Также получится сэкономить тем, у кого жилое помещение не оборудовано ИПУ.
    </p>
    <br>
    <p>
        А вот если в вашем жилом помещении установлены ИПУ газа, воды, то, уезжая в отпуск или на дачу, вы не сможете  ощутимо сэкономить на перерасчете за «коммуналку». Простой пример: только за нотариальный перевод документов на русский или белорусский язык придется заплатить около 50-60 рублей. В этой ситуации каждый сам решает, стоит ли такая экономия времени и денег. 
    </p>
    
