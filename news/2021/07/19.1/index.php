    </p>
    <br>
    <p>
        Еще один важный момент, которым граждане зачастую пренебрегают. Если будет доказано, что в период заявленного отсутствия вы все-таки приезжали домой на день-два, то перерасчет вам не сделают. В Положении четко сказано, что «в случае обнаружения возвращения гражданина к месту жительства в период, указанный как период отсутствия, гражданину производится начисление оплаты за коммунальные услуги». Факт возвращения оформляется соответствующим актом, и с этого момента будет начисляться плата.
