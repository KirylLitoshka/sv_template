    </p>
    <br>
    <br>
    <p>
        <b>
            По каким жилищно-коммунальным услугам можно рассчитывать на перерасчет?
        </b>
    </p>
    <br>
    <p>
       Для начала стоит обозначить, какие основные жилищно-коммунальные услуги оплачиваются вне зависимости от того, проживаем мы в данный момент в квартире или нет. Это техническое обслуживание, отчисления на капитальный ремонт, управление общим имуществом, техническое обслуживание лифта, санитарное содержание вспомогательных помещений и освещение мест общего пользования, плата за найм.
    </p>
    <br>
    <p>
        Право пользования или право собственности жилым помещением влекут определенные обязанности по содержанию в надлежащем виде имущества, по оплате жилищно-коммунальных услуг, по затратам на содержание общедомового имущества.
    </p>
    <br>
    <p>
        Кстати, если вы захотите сделать перерасчет зимой, то знайте, что за отопление вам его не сделают, так как отключить одну квартиру от отопления технически невозможно.
    </p>
    <br>
    <p>
        А по услугам таким, как обращение с твердыми коммунальными отходами, электроэнергия на работу лифта можно рассчитывать не перерасчет. Тут все логично и просто: человек не проживает, значит, мусора не производит. Примерно такая же ситуация и с оплатой электроэнергии за пользование лифтом: человек не пользуется лифтом, соответственно, он не потребляет электроэнергию на его движение.
    </p>
    <br>
    <p>
        Следует обратить внимание на порядок расчета и перерасчета по следующим услугам:
    </p>
    <p>
        - холодное водоснабжение
    </p>
    <p>
        - водоотведение (канализация)
    </p>
    <p>
        - подогрев воды
    </p>
    <p>
        - газоснабжение.
    </p>
