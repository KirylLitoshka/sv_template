    <p>
        Иногда  человек уезжает на длительный период, но не успевает или забывает посетить расчетный центр с заявлением.
    </p>
    <br>
    <p>
        В таком случае плата за оказание услуг будет начисляться, и ее нужно оплачивать в установленные законодательством сроки, в противном случае возникнет долг, который сверху «обрастет» еще и пеней.
