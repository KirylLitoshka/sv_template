<br><h1><strong>Глобальная акция «Час Земли»</strong></h1>
<h2>
<p><br>	


24 марта 2018 года в Беларуси в десятый раз пройдёт глобальная акция «Час Земли», организуемая по всему миру Всемирным фондом дикой природы (WWF). «Час Земли» является наиболее известной экологической акцией в истории человечества. В прошлом году в ней приняло участие более 2-х миллиардов человек из 187 стран мира, была выключена подсветка более чем 3100 архитектурных объектов. В Беларуси мероприятия прошли в 11 городах и деревнях, была отключена подсветка центральных архитектурных объектов.
<p><br>	
Выключая свет па один час (с 20.30 до 21.30 по местному времени) люди, компании и местные власти поднимают на поверхность проблемы изменения климата, энергосбережения и сохранения ресурсов в целом.
<p><br>	
Акция «Час Земли» ежегодно призывает людей изменить простые ежедневные привычки, экономно расходовать электроэнергию и тепло, что соответствует положениям Директивы Президента Республики Беларусь № 3 от 14 июня 2007 года.

