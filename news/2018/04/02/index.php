<h2>
<p><br>	

Работниками Гомельского областного управления Госпромнадзора во исполнение поручения Главы Государства по осуществлению комплекса надзорно-профилактических мероприятий на торгово-развлекательных объектах 27-28 марта проведены мониторинги 12-ти торговых и торгово-развлекательных центров в городе Гомеле и в районных центрах области. За указанный период обследованы 13 лифтов, 11 эскалаторов, 8 котельных.
<p><br>	В результате обследования котельной с 3-мя котлами на газовом топливе в ТЦ «Березки» (г. Светлогорск) выявлено 10 нарушений, 2 из которых влияют на безопасную эксплуатацию объекта: не назначены ответственные за исправное состояние и за безопасную эксплуатацию потенциально опасного объекта (котельной), а также не проведено техническое освидетельствование трех газовых котлов. Собственнику объекта внесено предложение о приостановлении эксплуатации котельной. Собственником предложение принято к исполнению – эксплуатация котельной приостановлена.


