<br><h1><strong>Профсоюз - Конкурс на лучшую новогоднюю стенгазету!</strong></h1>
<h2>	

<p><br>
В поисках новых форм работы  с членами профсоюза не стоит также забывать о хорошо известных, но незаслуженно забытых. В этом убедился профсоюзный комитет КЖУП «Светочь», объявив  в канун Нового года   конкурс на лучшую новогоднюю стенгазету.    
<p><br>
 Перед коллективами стояла задача оригинально и красочно поздравить всех с Новым 2018 годом. Работы были представлены как от отделов, так и в индивидуальном порядке.  Каждая стенгазета уникальна, в каждую вложен большой труд. Поэтому выбор победителей был непростым. В канун Старого Нового года, профсоюзный комитет подвел итоги конкурса "Новогодняя стенгазета».
 <p><br>
 <p> 1 место- семья Евсеевых 
 <p> 2 место- Лаборатория
 <p> 2 место- Шиманская Соня
 <p> 3 место- Литвякова Анна 
 <p> 3 место- Участок лифтового хозяйства
 <p> 3 место- центральная бухгалтерия
<p><br>
Победители были награждены грамотами и ценными призами
Поздравляем победителей! Также выражаем благодарность всем участникам конкурса и желаем дальнейших творческих успехов!
<p><br>
