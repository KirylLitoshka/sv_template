<p><br>
<img src="img/2018-11-13/2.jpg" width="700">
<p><br>
<b>Как будут оплачивать услуги  не занятые в экономике?</b>
<p><br>
Чтобы узнать, сколько придется платить за горячую воду, общее количество тепловой энергии будут делить на число зарегистрированных в квартире человек. 
<p><br>
Точно также иждивенцы будут оплачивать и отопление. Расчет будет производиться исходя из общей площади жилого помещения. Общие затраты поделят поровну, а безработный оплатит свою долю по обновленным тарифам.
<p><br>
Как иждивенцы будут оплачивать газоснабжение, будет зависеть, есть ли в жилище счетчик. Если индивидуальный прибор учета газа есть, то потребленный объем газа на семью разделят поровну между жильцами, а гражданин не занятый в экономике, оплатит свою «долю» по полному тарифу. При отсутствии счетчика общую площадь квартиры или дома разделят на количество прописанных, а тунеядец должен будет заплатить по экономически обоснованным тарифам.
<p><br>
За горячую воду безработные будут платить с 1 января 2019 года, а за газ и тепло — с 1 октября 2019 года.
<p><br>
<b>Почему именно эти услуги?</b>
<p><br>
Как объяснили в Министерстве ЖКХ, сегодня белорусы и так как никогда близки к тому, чтобы стопроцентно возмещать расходы за ЖКУ. Газ, отопление и горячая вода — те немногие услуги, которые только приближаются к оплате по себестоимости, подчеркнули в ведомстве.
<p><br>
<b>На сколько больше будут платить не занятые в экономике?</b>
<p><br>
Если в квартире живет только один неработающий: то плата за горячую воду вырастет на 5-6 рублей, отопление — на 60 рублей, а газ (если есть отдельная колонка или котел)— на 40 рублей.
<p><br>
В итоге жировка для безработного тяжелеет на 106 рублей.
<p><br>
Если с тунеядцем в квартире живут еще 3 человека, то расходы на отопление увеличиваются на 15 рублей, а за газ — на 16 рублей.
<p><br>
<b>Что будет, если тунеядец не будет платить за коммуналку?</b>
<p><br>
Если собственник жилья из числа безработных не станет платить за ЖКУ, его ждут стандартные в таких случаях штрафные санкции — пеня и предупреждения об отключении неоплаченных услуг. А как быть, если в квартире с безработным живут родственники? Скорее всего, оплачивать непогашенные счета будут именно они.
