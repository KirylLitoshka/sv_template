<br><h1><strong>Жировка тяжелеет на 106 рублей: как будут платить за ЖКУ трудоспособные граждане, не желающие работать?

</strong></h1>
<h2>

<p><br>

Горячая вода, отопление и газоснабжение. За эти услуги   неработающие граждане, у которых в собственности есть жильё, или арендаторы будут платить по повышенному тарифу. А если  откажутся платить, то оплачивать услуги за него должны родственники.
<p><br>
<b>Кто из неработающих граждан будет платить по «полной»?</b>
<p><br>
Требовать полную плату за ЖКУ будут только с тех безработных, кто является собственником жилья или на кого оформлен договор аренды. В остальных случаях платить по экономически обоснованным тарифам не занятые в экономике не будут.
<p><br>
Если жилье оформлено на работающего человека, а в семье есть безработный, то в этом случае коммуналку будут начислять по обычным тарифам. Например, владельцем квартиры является работающая мать, а её дочь не занята в экономике. В таком случае никаких повышенных тарифов не будет.
<p><br>
В случае, если владелец квартиры не занят в экономике и не живет по месту прописки ( а там проживает, например, его сын с семьей), тогда владелец обязан оплатить свою равную долю по экономически обоснованным тарифам.
<p><br>
А если у неработающего гражданина вовсе нет квартиры и он снимает жильё, то платить за коммунальные услуги «по полной» он не будет.
<p><br>
