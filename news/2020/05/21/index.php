Профсоюз - «Витаминная  акция» профсоюзного комитета КЖУП «Светочь»

</h1></strong>

<h2>
<p><br>


Ежегодно 15 мая в день Семьи комиссия содействия семье и школе собирала в профсоюзном комитете многодетных матерей-работниц предприятия. За чашкой чая шел неспешный разговор о проблемах воспитания, опытные мамочки делились секретами ведения домашнего хозяйства, специалисты юридической и кадровой службы давали необходимые консультации.
<p><br>
В нынешнем году пандемия не позволила провести встречу в обычном формате, и тогда родилась идея «витаминной акции». Профсоюзные активисты предложили собрать для многодетных семей продуктовые наборы. В их состав вошли соки, фрукты, детские завтраки и бутылочки с «Актимелем». Резонанс об этой акции мгновенно разлетелся по всему предприятию. Особенно приятно было слушать слова благодарности от многодетных родителей – их на предприятии 44 семьи, в которых воспитывается 151 ребенок.

<p><br>
Г.В.Маковская




