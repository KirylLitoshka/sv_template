<br><h1><strong>

В Светлогорском районе вводится запрет на посещение лесов

</h1></strong>

<h2>
	<p><br>

 <p><br>
 
С 8 апреля 2020 г. на территории Светлогорского района вводится запрет на посещение лесов. Ограничения вызваны сложной пожароопасной обстановкой в лесном фонде, обусловленной дефицитом влаги и сильным порывистым ветром.
 <p><br>
По оперативным данным, с начала года в Гомельской области зарегистрировано 69 случаев лесных пожаров на общей площади 224 га.
 <p><br>
На время действия запрета будут закрыты все шлагбаумы на съездах с дорог. Как пояснили специалисты Светлогорского РОЧС, «движение в лесах и на торфяниках запрещено для любого автотранспорта, кроме транспорта контроль-надзорных органов и техники лесхоза. Исключение составляют дороги общего пользования, ведущие к населенным пунктам, дачным кооперативам, водоемам и кладбищам».
 <p><br>
Тем, кто проигнорирует запрет, грозит штраф в размере до 25 базовых величин, а если действия нарушителей приведут к пожару — до 50 базовых.
 <p><br>
В случае обнаружения лесных пожаров просьба незамедлительно информировать дежурного по пожарам лесхоза по телефону 4-81-77 либо РОЧС по телефону 101.



