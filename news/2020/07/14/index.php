Профсоюз - Фотовыставка в КЖУП «Светочь»

</h1></strong>

<h2>
	<p><br>

«Мы- будущее Беларуси!» - выставка детских фотографий под таким девизом была организована комиссией содействия семье и школе профсоюзного комитета КЖУП «Светочь» в канун Дня Независимости Республики Беларусь.   Участников выставки попросили выяснить профессиональные предпочтения своих детей и представить их на фотографиях. Так в профкоме появились фото юных модельера, учителя математики, бухгалтера, сотрудницы МЧС и даже строгой начальницы, а также автогонщика, будущих Марадоны и Пеле, охотника и рыболова. Мир интересов наших детей оказался очень разнообразным. Мы не знаем, кем станут наши дети в будущем, сбудутся ли их детские мечты, но верим, что из них вырастут достойные граждане нашей страны.
<p><br>
При закрытии выставки все ее участники получили на память от профсоюзного комитета свои фотографии, а также кружки с фотографиями детей и сладкие подарки.

<p><br>
	Автор Г.В.Маковская





