<h2>
<p><br>
15 марта в Республике Беларусь отмечается День потребителя.
<p><br>
В 2020 году Всемирный день защиты прав потребителей проходит под девизом: «За ответственное потребление».
<p><br>
Продвижение идеи рационального и ответственного потребления является актуальной для жителей не только нашей республики, это глобальная проблема, решение которой зависит от участия каждого человека.
Истощение природных ресурсов и негативные последствия ухудшения состояния окружающей среды, включая опустынивание, засухи, деградацию земель, нехватку питьевой воды и утрату биоразнообразия, приумножают и обостряют многочисленные проблемы, стоящие перед человечеством. Для того чтобы сохранить природные ресурсы и улучшить социальные условия для нынешних и будущих поколений, всемирная организация потребителей призывает каждого потребителя придерживаться принципов ответственного потребления товаров и услуг. Это в значительной степени позволит уменьшить будущие экономические, экологические и социальные издержки, повысит экономическую конкурентоспособность.




