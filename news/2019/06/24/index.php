<br><h1><strong>Профсоюз - За безопасный труд в строительной сфере
</strong></h1>
<h2>
<p><br>	

2019 год в республике Беларусь объявлен годом безопасного труда в строительстве. Этой теме был посвящен областной день охраны труда, прошедший в мае 20-19г. в  Светлогорске. Одним из объектов показа стали производственные территории ремонтно-строительного управления «Светлогорскремстрой».
<p><br>	
Профсоюзный комитет КЖУП «Светочь» также уделяет внимание вопросам охраны труда в строительстве. Совместно с отделом охраны труда профком объявил в июне акцию «Позаботься о свое безопасности». В рамках акции проводятся рейды комиссии по охране труда и специалистов отдела ОТ по строительным объектам. Профсоюзный комитет в помощь общественным инспекторам по охране труда  разработал буклет «Позаботься о своей безопасности», который роздан на все строительные объекты. Сейчас профком и общественная комиссия по охране труда  подготовила такой же буклет для работников электроцеха .
<p><br>	
Председатель профкома Г.Маковская





