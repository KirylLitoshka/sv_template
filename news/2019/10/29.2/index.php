<h2>
<p><br>
В целях оказания помощи поднадзорным организациям и предупреждения аварий, инцидентов и несчастных случаев работники Гомельского областного управления Госпромнадзора проводят разъяснительную, профилактическую работу в трудовых коллективах о порядке соблюдения требований законодательства в области промышленной безопасности.
