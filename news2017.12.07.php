<br><h1><strong>Финальные соревнования спартакиады</strong></h1>
<p><br>

<h2>	
В 2017 году круглогодичная спартакиада среди структурных подразделений коммунального жилищного унитарного предприятия «Светочь» отмечает пятнадцатилетний юбилей. За эти годы выработалась стройная система проведения межцеховых турниров и первенств, подведения итогов, чествования победителей. 
<p><br>
Традиционно финальным этапом спартакиады стали соревнования по плаванью, которые прошли 6 декабря в бассейне СДЮСШОР №1 г.Светлогорска.  В соревнованиях приняли участие 8 сборных команд, в составе каждой – 2 мужчин и 1 женщина. Победителем соревнований стала команда цеха ВКХ, в составе которой – неоднократные призеры областной спартакиады Анна Берлина и Виктор Корзун и молодой спортсмен Дима Новицкий. На втором месте – команда ремонтно-строительного управления «Светлогорскремстрой», на третьем- сборная команда жилфонда, костяк которой составляют спортсмены участка текущего обслуживания жилфонда – Вадим Каминский и Артур Навоев.
<p><br>
Но интрига спартакиады еще сохраняется, ведь последними будут отстаивать честь своих подразделений руководители в новогоднем турнире по боулингу.
<p><br>
Автор  председатель профкома Г.Маковская 

 

